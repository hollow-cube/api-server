package gen

import (
	"bytes"
	"fmt"
	"go/format"
	"regexp"
)

// GenerateServer produces the server.gen.go glue code from the analyzed API.
func GenerateServer(api *API) ([]byte, error) {
	var buf bytes.Buffer

	runtimeImport := api.ModulePath + "/pkg/ox/runtime"
	oxImport := api.ModulePath + "/pkg/ox"
	needsOx := needsOxImport(api)

	fmt.Fprintf(&buf, "// Code generated by ox. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", api.PackageName)

	// Imports
	fmt.Fprintf(&buf, "import (\n")
	if needsIO(api) {
		fmt.Fprintf(&buf, "\t\"io\"\n")
	}
	fmt.Fprintf(&buf, "\t\"net/http\"\n")
	if needsStrconv(api) {
		fmt.Fprintf(&buf, "\t\"strconv\"\n")
	}
	fmt.Fprintf(&buf, "\n")
	if needsOx {
		fmt.Fprintf(&buf, "\t%q\n", oxImport)
	}
	fmt.Fprintf(&buf, "\t%q\n", runtimeImport)
	fmt.Fprintf(&buf, ")\n\n")

	// RegisterParams type
	fmt.Fprintf(&buf, "// RegisterParams configures route registration.\n")
	fmt.Fprintf(&buf, "type RegisterParams struct {\n")
	fmt.Fprintf(&buf, "\tMux     *http.ServeMux\n")
	fmt.Fprintf(&buf, "\tBaseURL string // Optional prefix for all routes\n")
	fmt.Fprintf(&buf, "}\n\n")

	// RegisterRoutes function
	fmt.Fprintf(&buf, "// RegisterRoutes registers all API routes on the given ServeMux.\n")
	fmt.Fprintf(&buf, "func RegisterRoutes(s *%s, params RegisterParams) {\n", api.StructName)
	fmt.Fprintf(&buf, "\th := &handlers{server: s}\n")
	for _, ep := range api.Endpoints {
		fmt.Fprintf(&buf, "\tparams.Mux.HandleFunc(%q+params.BaseURL+%q, h.%s)\n", ep.Method+" ", muxPath(ep.Path), lcFirst(ep.Name))
	}
	fmt.Fprintf(&buf, "}\n\n")

	// Handlers struct
	fmt.Fprintf(&buf, "// handlers wraps the server and provides HTTP handler methods.\n")
	fmt.Fprintf(&buf, "type handlers struct {\n")
	fmt.Fprintf(&buf, "\tserver *%s\n", api.StructName)
	fmt.Fprintf(&buf, "}\n\n")

	// Handler methods
	for i := range api.Endpoints {
		writeHandler(&buf, api, &api.Endpoints[i])
	}

	return format.Source(buf.Bytes())
}

func writeHandler(buf *bytes.Buffer, api *API, ep *Endpoint) {
	// Generate handler method on the handlers struct
	fmt.Fprintf(buf, "func (h *handlers) %s(w http.ResponseWriter, r *http.Request) {\n", lcFirst(ep.Name))

	// Declare and populate request struct if it has params or an embedded body
	hasReqStruct := ep.RequestType != ""
	hasParams := len(ep.Params) > 0
	hasEmbeddedBody := ep.RequestBody != nil && ep.RequestBody.GoName != "body"

	if hasReqStruct && (hasParams || hasEmbeddedBody) {
		fmt.Fprintf(buf, "\tvar req %s\n", ep.RequestType)
		for i := range ep.Params {
			writeParamBinding(buf, &ep.Params[i])
		}
		// If request struct has a Body field or embedded body, decode it
		if ep.RequestBody != nil && ep.RequestBody.GoName != "body" {
			switch {
			case ep.RequestBody.IsStream:
				writeStreamBodyAssign(buf, "req."+ep.RequestBody.GoName)
			case ep.RequestBody.IsReader:
				fmt.Fprintf(buf, "\treq.%s = r.Body\n", ep.RequestBody.GoName)
			case ep.RequestBody.IsRawBytes:
				writeRawBytesBodyAssign(buf, "req."+ep.RequestBody.GoName)
			default:
				fmt.Fprintf(buf, "\tif err := runtime.DecodeJSON(r, &req.%s); err != nil {\n", ep.RequestBody.GoName)
				fmt.Fprintf(buf, "\t\truntime.WriteBadRequest(w, \"invalid request body\")\n")
				fmt.Fprintf(buf, "\t\treturn\n")
				fmt.Fprintf(buf, "\t}\n")
			}
		}
	}

	// Decode separate body parameter if present (separate "body" parameter, not part of request struct)
	var bodyVar string
	if ep.RequestBody != nil && ep.RequestBody.GoName == "body" {
		bodyVar = ep.RequestBody.GoName
		switch {
		case ep.RequestBody.IsStream:
			fmt.Fprintf(buf, "\t%s := &ox.Stream{\n", bodyVar)
			fmt.Fprintf(buf, "\t\tContentType:   r.Header.Get(\"Content-Type\"),\n")
			fmt.Fprintf(buf, "\t\tBody:          r.Body,\n")
			fmt.Fprintf(buf, "\t\tContentLength: r.ContentLength,\n")
			fmt.Fprintf(buf, "\t}\n")
		case ep.RequestBody.IsReader:
			fmt.Fprintf(buf, "\t%s := r.Body\n", bodyVar)
		case ep.RequestBody.IsRawBytes:
			fmt.Fprintf(buf, "\t%s, err := io.ReadAll(r.Body)\n", bodyVar)
			fmt.Fprintf(buf, "\tif err != nil {\n")
			fmt.Fprintf(buf, "\t\truntime.WriteBadRequest(w, \"failed to read request body\")\n")
			fmt.Fprintf(buf, "\t\treturn\n")
			fmt.Fprintf(buf, "\t}\n")
		default:
			fmt.Fprintf(buf, "\tvar %s %s\n", bodyVar, ep.RequestBody.GoType)
			fmt.Fprintf(buf, "\tif err := runtime.DecodeJSON(r, &%s); err != nil {\n", bodyVar)
			fmt.Fprintf(buf, "\t\truntime.WriteBadRequest(w, \"invalid request body\")\n")
			fmt.Fprintf(buf, "\t\treturn\n")
			fmt.Fprintf(buf, "\t}\n")
		}
	}

	// Call the handler method
	callArgs := "r.Context()"
	if hasReqStruct {
		callArgs += ", req"
	}
	if bodyVar != "" {
		callArgs += ", " + bodyVar
	}

	hasResp := ep.Response.IsStream || ep.Response.GoType != ""
	useResp := hasResp && ep.Response.StatusCode != 204

	if useResp {
		fmt.Fprintf(buf, "\tresp, err := h.server.%s(%s)\n", ep.Name, callArgs)
	} else if hasResp {
		fmt.Fprintf(buf, "\t_, err := h.server.%s(%s)\n", ep.Name, callArgs)
	} else {
		fmt.Fprintf(buf, "\terr := h.server.%s(%s)\n", ep.Name, callArgs)
	}

	// Error handling
	fmt.Fprintf(buf, "\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\truntime.HandleError(w, err)\n")
	fmt.Fprintf(buf, "\t\treturn\n")
	fmt.Fprintf(buf, "\t}\n")

	// Write response
	switch {
	case !useResp:
		fmt.Fprintf(buf, "\tw.WriteHeader(%d)\n", ep.Response.StatusCode)
	case ep.Response.IsSSE:
		fmt.Fprintf(buf, "\truntime.WriteSSE[%s](w, r, resp)\n", ep.Response.SSEPayloadGoType)
	case ep.Response.IsStream:
		fmt.Fprintf(buf, "\truntime.WriteStream(w, %d, resp)\n", ep.Response.StatusCode)
	case ep.Response.ContentType == "text/plain":
		fmt.Fprintf(buf, "\truntime.WriteText(w, %d, resp)\n", ep.Response.StatusCode)
	default:
		fmt.Fprintf(buf, "\truntime.WriteJSON(w, %d, resp)\n", ep.Response.StatusCode)
	}

	fmt.Fprintf(buf, "}\n\n")
}

// writeStreamBodyAssign emits an assignment that constructs an *ox.Stream from
// the incoming http.Request and stores it at the given lvalue.
func writeStreamBodyAssign(buf *bytes.Buffer, lvalue string) {
	fmt.Fprintf(buf, "\t%s = &ox.Stream{\n", lvalue)
	fmt.Fprintf(buf, "\t\tContentType:   r.Header.Get(\"Content-Type\"),\n")
	fmt.Fprintf(buf, "\t\tBody:          r.Body,\n")
	fmt.Fprintf(buf, "\t\tContentLength: r.ContentLength,\n")
	fmt.Fprintf(buf, "\t}\n")
}

// writeRawBytesBodyAssign emits a read of the request body into the given
// lvalue (a []byte field on the request struct). On read failure the
// generated handler returns 400.
func writeRawBytesBodyAssign(buf *bytes.Buffer, lvalue string) {
	fmt.Fprintf(buf, "\tif b, err := io.ReadAll(r.Body); err != nil {\n")
	fmt.Fprintf(buf, "\t\truntime.WriteBadRequest(w, \"failed to read request body\")\n")
	fmt.Fprintf(buf, "\t\treturn\n")
	fmt.Fprintf(buf, "\t} else {\n")
	fmt.Fprintf(buf, "\t\t%s = b\n", lvalue)
	fmt.Fprintf(buf, "\t}\n")
}

func writeParamBinding(buf *bytes.Buffer, p *Param) {
	switch p.Location {
	case "path":
		writePathParam(buf, p)
	case "query":
		writeQueryParam(buf, p)
	case "header":
		fmt.Fprintf(buf, "\treq.%s = r.Header.Get(%q)\n", p.GoName, p.Name)
	}
}

func writePathParam(buf *bytes.Buffer, p *Param) {
	// Path parameters are not optional and not pointers — there is no way
	// to express absence in a URL path. We only need the simple cases.
	source := fmt.Sprintf("r.PathValue(%q)", p.Name)
	switch p.GoType {
	case "string":
		if needsCast(p) {
			fmt.Fprintf(buf, "\treq.%s = %s(%s)\n", p.GoName, p.ElemType, source)
		} else {
			fmt.Fprintf(buf, "\treq.%s = %s\n", p.GoName, source)
		}
	case "int", "int64", "bool":
		writeParseAndAssign(buf, p, "path", source, "req."+p.GoName)
	}
}

func writeQueryParam(buf *bytes.Buffer, p *Param) {
	source := fmt.Sprintf("r.URL.Query().Get(%q)", p.Name)

	// Pointer query params: gate on non-empty, allocate, assign pointer.
	if p.IsPointer {
		fmt.Fprintf(buf, "\tif qs := %s; qs != \"\" {\n", source)
		switch p.GoType {
		case "string":
			if needsCast(p) {
				fmt.Fprintf(buf, "\t\tv := %s(qs)\n", p.ElemType)
			} else {
				fmt.Fprintf(buf, "\t\tv := qs\n")
			}
			fmt.Fprintf(buf, "\t\treq.%s = &v\n", p.GoName)
		case "int", "int64", "bool":
			writeParseAndAssignPtr(buf, p, "qs")
		}
		fmt.Fprintf(buf, "\t}\n")
		return
	}

	switch p.GoType {
	case "string":
		if needsCast(p) {
			fmt.Fprintf(buf, "\treq.%s = %s(%s)\n", p.GoName, p.ElemType, source)
		} else {
			fmt.Fprintf(buf, "\treq.%s = %s\n", p.GoName, source)
		}
	case "int", "int64", "bool":
		if p.Required {
			writeParseAndAssign(buf, p, "query", source, "req."+p.GoName)
		} else {
			fmt.Fprintf(buf, "\tif qs := %s; qs != \"\" {\n", source)
			writeParseAndAssignIndented(buf, p, "qs", "req."+p.GoName)
			fmt.Fprintf(buf, "\t}\n")
		}
	}
}

// needsCast reports whether the field's concrete (assigned) type differs
// from the basic kind being parsed. Old code paths and tests construct
// Param values with only GoType set, so an empty ElemType is treated as
// equivalent to GoType (no cast needed).
func needsCast(p *Param) bool {
	return p.ElemType != "" && p.ElemType != p.GoType
}

// writeParseAndAssign emits a parse+assign block for required (non-pointer)
// numeric/bool params at top indent. The source expression is parsed; on
// failure WriteBadRequest is returned, otherwise v is assigned to dst (cast
// through ElemType when the field is a named type).
func writeParseAndAssign(buf *bytes.Buffer, p *Param, location, source, dst string) {
	parser := parserCall(p.GoType, source)
	fmt.Fprintf(buf, "\tif v, err := %s; err != nil {\n", parser)
	fmt.Fprintf(buf, "\t\truntime.WriteBadRequest(w, %q)\n", "invalid "+location+" parameter: "+p.Name)
	fmt.Fprintf(buf, "\t\treturn\n")
	fmt.Fprintf(buf, "\t} else {\n")
	if needsCast(p) {
		fmt.Fprintf(buf, "\t\t%s = %s(v)\n", dst, p.ElemType)
	} else {
		fmt.Fprintf(buf, "\t\t%s = v\n", dst)
	}
	fmt.Fprintf(buf, "\t}\n")
}

// writeParseAndAssignIndented is like writeParseAndAssign but nested one
// level deeper (inside an `if qs := ...; qs != "" {` block).
func writeParseAndAssignIndented(buf *bytes.Buffer, p *Param, source, dst string) {
	parser := parserCall(p.GoType, source)
	fmt.Fprintf(buf, "\t\tif v, err := %s; err != nil {\n", parser)
	fmt.Fprintf(buf, "\t\t\truntime.WriteBadRequest(w, %q)\n", "invalid query parameter: "+p.Name)
	fmt.Fprintf(buf, "\t\t\treturn\n")
	fmt.Fprintf(buf, "\t\t} else {\n")
	if needsCast(p) {
		fmt.Fprintf(buf, "\t\t\t%s = %s(v)\n", dst, p.ElemType)
	} else {
		fmt.Fprintf(buf, "\t\t\t%s = v\n", dst)
	}
	fmt.Fprintf(buf, "\t\t}\n")
}

// writeParseAndAssignPtr emits a parse+assign-pointer block for pointer
// numeric/bool params, indented one level (inside the non-empty gate).
func writeParseAndAssignPtr(buf *bytes.Buffer, p *Param, source string) {
	parser := parserCall(p.GoType, source)
	fmt.Fprintf(buf, "\t\tv, err := %s\n", parser)
	fmt.Fprintf(buf, "\t\tif err != nil {\n")
	fmt.Fprintf(buf, "\t\t\truntime.WriteBadRequest(w, %q)\n", "invalid query parameter: "+p.Name)
	fmt.Fprintf(buf, "\t\t\treturn\n")
	fmt.Fprintf(buf, "\t\t}\n")
	if needsCast(p) {
		fmt.Fprintf(buf, "\t\tcv := %s(v)\n", p.ElemType)
		fmt.Fprintf(buf, "\t\treq.%s = &cv\n", p.GoName)
	} else {
		fmt.Fprintf(buf, "\t\treq.%s = &v\n", p.GoName)
	}
}

// wildcardSeg matches a {*name} path segment in a route string.
var wildcardSeg = regexp.MustCompile(`\{\*([A-Za-z_][A-Za-z0-9_]*)\}`)

// muxPath translates a route path containing {*name} wildcard segments into
// the net/http ServeMux equivalent {name...}. Non-wildcard segments are
// preserved verbatim.
func muxPath(path string) string {
	return wildcardSeg.ReplaceAllString(path, "{$1...}")
}

// parserCall returns the strconv call for the given basic kind.
func parserCall(kind, source string) string {
	switch kind {
	case "int":
		return fmt.Sprintf("strconv.Atoi(%s)", source)
	case "int64":
		return fmt.Sprintf("strconv.ParseInt(%s, 10, 64)", source)
	case "bool":
		return fmt.Sprintf("strconv.ParseBool(%s)", source)
	}
	return ""
}

func needsStrconv(api *API) bool {
	for _, ep := range api.Endpoints {
		for _, p := range ep.Params {
			if p.GoType != "string" {
				return true
			}
		}
	}
	return false
}

// needsIO reports whether the generated file imports "io" — currently only
// when a request body is bound as raw []byte (which uses io.ReadAll).
func needsIO(api *API) bool {
	for _, ep := range api.Endpoints {
		if ep.RequestBody != nil && ep.RequestBody.IsRawBytes {
			return true
		}
	}
	return false
}

// needsOxImport reports whether the generated server file references the
// pkg/ox package directly (currently only for constructing &ox.Stream{} when
// binding stream request bodies).
func needsOxImport(api *API) bool {
	for _, ep := range api.Endpoints {
		if ep.RequestBody != nil && ep.RequestBody.IsStream {
			return true
		}
	}
	return false
}
